#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub enum Token {
    #[default]
    Illegal,
    Eof,
    Identifier(String),
    //Literals
    Int(i64),
    True,
    False,
    //Operators
    Eq,
    NotEq,
    Assign,
    Plus,
    Minus,
    Bang,
    Asterisk,
    Slash,
    Lt,
    Gt,
    //Delimiters
    Comma,
    Semicolon,
    LParen,
    RParen,
    LBrace,
    RBrace,
    //Keywords
    Function,
    Let,
    If,
    Else,
    Return,
}
